package com.oa.personnel.entity;

public class FormNo {
	private int empNo;
	private int disNo;
	private int appNo;
	private int reportNo;
	public int getEmpNo() {
		return empNo;
	}
	public void setEmpNo(int empNo) {
		this.empNo = empNo;
	}
	public int getDisNo() {
		return disNo;
	}
	public void setDisNo(int disNo) {
		this.disNo = disNo;
	}
	public int getAppNo() {
		return appNo;
	}
	public void setAppNo(int appNo) {
		this.appNo = appNo;
	}
	public int getReportNo() {
		return reportNo;
	}
	public void setReportNo(int reportNo) {
		this.reportNo = reportNo;
	}


}
